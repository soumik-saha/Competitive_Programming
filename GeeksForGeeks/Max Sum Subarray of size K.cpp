class Solution{   
public:
    long maximumSumSubarray(int K, vector<int> &Arr , int N){
        // code here 
        long ans = 0;
        for(int j=0; j<K; j++) {
            ans += Arr[j];
        }
        
        long sum = ans;
        for(int i=K, j=0; i<Arr.size(); i++, j++) {
            sum += Arr[i] - Arr[j];
            ans = max(ans, sum);
        }
        
        return ans;
    }
};

