class Solution {
  public:
    // Function to sort the given doubly linked list using Merge Sort.
    struct Node *sortDoubly(struct Node *head) {
        // Your code here
        vector<int> v;
        while(head) {
            v.push_back(head->data);
            head = head->next;
        }
        
        sort(v.begin(), v.end());
        Node* ptr = new Node(v[0]);;
        Node* anshead = ptr;
        for(int i=1; i<v.size(); i++) {
            Node* newNode = new Node(v[i]);
            newNode->prev = ptr;
            ptr->next = newNode;
            ptr = newNode;
        }
        
        return anshead;
    }
};


