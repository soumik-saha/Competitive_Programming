/* matrix : given input matrix, you are require
 to change it in place without using extra space */
void rotate(vector<vector<int> >& mat) {
    // Your code goes here
    int m = mat.size(), n = mat[0].size();
    vector<vector<int>> v;
    for(int i=0; i<n; i++) {
        vector<int> arr;
        for(int j=m-1; j>=0; j--) {
            arr.push_back(mat[j][i]);
        }
        v.push_back(arr);
    }
    mat = v;
}


