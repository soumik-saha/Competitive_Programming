class Solution {
  public:
    int countPairs(vector<int> &arr, int target) {
        // Code here
        int n = arr.size();
        int count = 0;
        unordered_map<int, int> freq;
        for(int i=0; i<n; i++) {
            if(freq.find(target-arr[i])!=freq.end()) {
                count += freq[target-arr[i]];
            }
            freq[arr[i]]++;
        }
        return count;
    }
};

