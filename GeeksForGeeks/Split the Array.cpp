// User function template for C++
class Solution {
  public:
    int countgroup(vector<int>& arr) {
        // Complete the function
        int n = arr.size();
        
        int ans = 0;
        
        for(int i=0; i<n; i++) {
            ans ^= arr[i];
        }
        
        return ans==0 ? (1<<(n-1))-1 : 0;
    }
};

