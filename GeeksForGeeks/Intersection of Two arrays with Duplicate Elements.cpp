class Solution {
  public:
    vector<int> intersectionWithDuplicates(vector<int>& a, vector<int>& b) {
        // code here
        vector<int> ans;
        unordered_set<int> st(a.begin(), a.end()), st2(b.begin(), b.end());
        for(auto e:st2) {
            if(st.find(e)==st.end())    continue;
            ans.push_back(e);
        }
        return ans;
    }
};

