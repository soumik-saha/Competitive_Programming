// User function template for C++
class Solution {
  public:
    /* Returns count of pairs with difference k  */
    int countPairsWithDiffK(vector<int>& arr, int k) {
        // code here
        unordered_map<long long, long long> mp;
        long long ans = 0;
        for(long long ele:arr) {
            if(mp.find(ele-k)!=mp.end())
                ans += mp[ele-k];
            if(mp.find(ele+k)!=mp.end())
                ans += mp[ele+k];
            
            mp[ele]++;
        }
        return ans;
    }
};

