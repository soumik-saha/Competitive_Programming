class Solution {
  public:
    int maximumProfit(vector<int> &prices) {
        // code here
        int ans = 0;
        int n = prices.size();
        int minPrice = INT_MAX;
        for(int i=0; i<n; i++) {
            if(minPrice>prices[i]) {
                minPrice = prices[i];
            }
            else {
                ans = max(ans, prices[i]-minPrice);
            }
        }
        return ans;
    }
};


